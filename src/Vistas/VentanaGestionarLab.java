    private void cargarTablaEstudiante() {
        DefaultTableModel model = new DefaultTableModel();
        String[] columnas = new String[]{
            "Codigo universitario", "nombre", "apellido", "cedula", "celular"
        };
        model.setColumnIdentifiers(columnas);
        for (int i = 0; i < laboratorio.getEstudiantes().getSize(); i++) {
            model.addRow(new Object[]{
                laboratorio.getEstudiantes().obtenerDatoIndice(i).getCodigoUniversitario(),
                laboratorio.getEstudiantes().obtenerDatoIndice(i).getNombre(),
                laboratorio.getEstudiantes().obtenerDatoIndice(i).getApellido(),
                laboratorio.getEstudiantes().obtenerDatoIndice(i).getCedula(),
                laboratorio.getEstudiantes().obtenerDatoIndice(i).getNumeroCelular(),});
        }

        TablaEstudiantesEnLaboratorio.setModel(model);
    }

    private void btnDeshabilitarLaboratorioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeshabilitarLaboratorioMouseClicked
        if (laboratorio.getDisponibilidad().equals(Laboratorio.DISPONIBLE)) {
            if (laboratorio.getEstudiantes().getSize() == 0) {
                String motivo = JOptionPane.showInputDialog(null, "Escriba el motivo por el cual deshabilitara el labatorio");
                laboratorio.setMotivo(motivo);
                controladorVentanaAdministradorLaboratorio.deshabilitarLaboratorio(fila, columna);
                JOptionPane.showMessageDialog(null, "Laboratorio deshabilitado correctamente.");
                ventanaLaboratorio.setVisible(true);
                this.dispose();
                ventanaLaboratorio.validarPosiciones();
            } else {
                JOptionPane.showMessageDialog(null, "El laboratorio no se puede deshabilitar ya que hay un estudiante dentro.");
            }
        }
    }//GEN-LAST:event_btnDeshabilitarLaboratorioMouseClicked

    private String cupoLaboratorio() {
        int contador = 0;
        for (int i = 0; i < laboratorio.getEstudiantes().getSize(); i++) {
            contador = i + 1;
        }
        return String.valueOf(contador);
    }

    private void btnIngresarEstudianteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIngresarEstudianteMouseClicked
        String codigoUniversitario = txtCodigoUniversitario.getText();
        Persona persona = controladorVentanaAdministradorLaboratorio.buscarPersonaCodigo(codigoUniversitario);
        if (persona instanceof Estudiante) {
            Estudiante estudiante = (Estudiante) persona;
            if (controladorVentanaAdministradorLaboratorio.obtenerLaboratorio(fila, columna).buscarEstudianteTodosLosLaboratorios(estudiante.getCodigoUniversitario()) == null) {
                if (controladorVentanaAdministradorLaboratorio.obtenerLaboratorio(fila, columna).getCupo() == controladorVentanaAdministradorLaboratorio.obtenerLaboratorio(fila, columna).getEstudiantes().getSize()) {
                    JOptionPane.showMessageDialog(null, "El laboratorio se encuentra lleno, por lo cual el estudiante: " + estudiante.getNombre() + " pasara a la cola de espera.");
                    controladorVentanaAdministradorLaboratorio.registrarColaLaboraorio(fila, columna, estudiante);
                    jLabelNombreEstudianteEspera.setText(estudiantesEnCola());
                } else {
                    controladorVentanaAdministradorLaboratorio.regLab(fila, columna, estudiante);
                }
            } else {
                JOptionPane.showMessageDialog(null, "El estudiante: " + estudiante.getNombre() + " ya se encuentra en un laboratorio por lo cual no se puede agregar a este");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se puede agregar debido a que solo se reliza registro de ingreso de estudiantes y no de docentes");
        }
        cargarTablaEstudiante();
        jLabelNombreEstudianteEspera.setText(estudiantesEnCola());
        cupoMaximo();
    }//GEN-LAST:event_btnIngresarEstudianteMouseClicked

