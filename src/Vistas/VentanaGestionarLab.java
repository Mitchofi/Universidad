    public String estudiantesEnCola() {
        if (controladorVentanaAdministradorLaboratorio.obtenerLaboratorio(fila, columna).getCola().getCabeza() != null) {
            return controladorVentanaAdministradorLaboratorio.obtenerLaboratorio(fila, columna).obtenerDatoCola().getNombre();
        }
        return "Ninguno";
    }

    public void cupoMaximo() {
        jLabelCupoMaximo.setText(laboratorio.getCupo() + "/ " + cupoLaboratorio());
    }

    private void btnEliminarIngresoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarIngresoMouseClicked
        if (txtCodigoUniversitario.equals("Codigo univeristario") || txtCodigoUniversitario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecciona/escribe el codigo universitario"
                    + "del estudiante que deseas eliminar.");
        } else {
            Persona persona = controladorVentanaAdministradorLaboratorio.buscarPersonaCodigo(txtCodigoUniversitario.getText());
            Estudiante estudiante = (Estudiante) persona;
            if (estudiante != null) {
                controladorVentanaAdministradorLaboratorio.eliminarIngresoLaboratorio(estudiante.getCodigoUniversitario(), fila, columna);
                cargarTablaEstudiante();
                JOptionPane.showMessageDialog(null, "El estudiante se elimino correctamente.");
                jLabelNombreEstudianteEspera.setText(estudiantesEnCola());
                cupoMaximo();
            } else {
                JOptionPane.showMessageDialog(null, "El estudiante no se pudo eliminar.");
            }
        }
    }//GEN-LAST:event_btnEliminarIngresoMouseClicked

    private void btnPrestarHerramientasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPrestarHerramientasMouseClicked
        if (txtCodigoUniversitario.getText().equals("Codigo Universitario")
                || txtCodigoUniversitario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecciona el estudiante al que deseas prestar"
                    + "la herramientas");
        } else {
            String codigo = txtCodigoUniversitario.getText();
            Persona persona = controladorVentanaAdministradorLaboratorio.buscarPersonaCodigoLab(codigo, fila, columna);
            if (persona != null) {
                Estudiante estudiante = (Estudiante) persona;
                VentanaRealizarPrestamo ventanaRealizarPrestamo = new VentanaRealizarPrestamo(this, estudiante, fila, columna);
                ventanaRealizarPrestamo.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "el estudiante no se encuentra dentro de laboratorio");
            }
        }
    }//GEN-LAST:event_btnPrestarHerramientasMouseClicked

