    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        if (txtNombreHerramienta.getText().equals("Nombre herramienta") || txtCodigoHerramienta.getText().equals("Codigo herramienta")) {
            JOptionPane.showMessageDialog(null, "Por favor selecciona/ la herramienta a eliminar");
            txtNombreHerramienta.setText("Nombre herramienta");
            txtNombreHerramienta.setForeground(Color.gray);
            txtCodigoHerramienta.setText("Codigo herramienta");
            txtCodigoHerramienta.setForeground(Color.gray);
        } else {
            String codigoHerramienta = txtCodigoHerramienta.getText();
            if (controladorVentanaAdministradorLaboratorio.eliminarHerramienta(codigoHerramienta)) {
                JOptionPane.showMessageDialog(null, "La herramienta con el codigo: " + codigoHerramienta + " fue eliminado correctamente");
                txtNombreHerramienta.setText("Nombre herramienta");
                txtNombreHerramienta.setForeground(Color.gray);
                txtCodigoHerramienta.setText("Codigo herramienta");
                txtCodigoHerramienta.setForeground(Color.gray);
            } else {
                JOptionPane.showMessageDialog(null, "La herramienta con el codigo: " + codigoHerramienta + " no pudo ser elimnado");
                txtNombreHerramienta.setText("Nombre herramienta");
                txtNombreHerramienta.setForeground(Color.gray);
                txtCodigoHerramienta.setText("Codigo herramienta");
                txtCodigoHerramienta.setForeground(Color.gray);
            }
            cargarTablaHerramienta();
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void tablaHerramientasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaHerramientasMouseClicked
        int seleccion = tablaHerramientas.rowAtPoint(evt.getPoint());
        String codigoHerramienta = tablaHerramientas.getValueAt(seleccion, 0).toString();
        String nombreHerramienta = tablaHerramientas.getValueAt(seleccion, 1).toString();
        txtCodigoHerramienta.setText(codigoHerramienta);
        txtNombreHerramienta.setText(nombreHerramienta);
    }//GEN-LAST:event_tablaHerramientasMouseClicked


