    public void cargarTablaHerramienta() {
        DefaultTableModel model = new DefaultTableModel();
        String[] columnas = new String[]{
            "Codigo herramienta", "nombre", "Disponibilidad"
        };
        model.setColumnIdentifiers(columnas);
        for (int i = 0; i < controladorVentanaAdministradorLaboratorio.getHerramientas().getSize(); i++) {
            model.addRow(new Object[]{
                controladorVentanaAdministradorLaboratorio.getHerramientas().obtenerDatoIndice(i).getCodigoHerramienta(),
                controladorVentanaAdministradorLaboratorio.getHerramientas().obtenerDatoIndice(i).getNombre(),
                controladorVentanaAdministradorLaboratorio.getHerramientas().obtenerDatoIndice(i).getDisponibilidad(),});
        }
        tablaHerramientas.setModel(model);
    }

    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        if (txtNombreHerramienta.getText().equals("Nombre herramienta")) {
            JOptionPane.showMessageDialog(null, "Por favor rellena los datos");
        } else {
            int auxCodigoHerramienta = Universidad.codigoHerramienta;
            String codigoHerramienta = "herramienta" + auxCodigoHerramienta;
            String nombreHerramienta = txtNombreHerramienta.getText();
            Herramienta herramienta = new Herramienta(nombreHerramienta, codigoHerramienta);
            if (controladorVentanaAdministradorLaboratorio.registrarHerramienta(herramienta)) {
                JOptionPane.showMessageDialog(null, "se registro correctamente la herramienta: " + herramienta.getNombre());
                txtNombreHerramienta.setText("Nombre herramienta");
                txtNombreHerramienta.setForeground(Color.gray);
                txtCodigoHerramienta.setText("Codigo herramienta");
                txtCodigoHerramienta.setForeground(Color.gray);
            } else {
                JOptionPane.showMessageDialog(null, "Ya se encuentra registrada la maxima cantidad de herramienta de ese tipo: " + herramienta.getNombre()
                        + " por lo cual no puedes registrar mas");
                txtNombreHerramienta.setText("Nombre herramienta");
                txtNombreHerramienta.setForeground(Color.gray);
                txtCodigoHerramienta.setText("Codigo herramienta");
                txtCodigoHerramienta.setForeground(Color.gray);
            }
            cargarTablaHerramienta();
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

